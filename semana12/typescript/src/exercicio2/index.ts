function adicao(n1:number, n2:number){
  const resultado: number = n1 + n2;  
  return console.log(resultado)

}

function subtracao(n1:number, n2:number){
  const resultado: number = n1 - n2;  
  return console.log(resultado)
}

function multiplicao(n1:number, n2:number){
    const resultado: number = n1 * n2;  
    return console.log(resultado)
}


function oMaior(n1:number, n2:number){
    const resultado: number = n1 > n2? n1 : n2; 
    return console.log(resultado)
}
